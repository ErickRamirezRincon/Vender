package mx.com.fincomun.vendedores.bean.request;

public class QiuboRecargasRequest {
	
	private String mobileNumber;
	private String amount;
	private String codeCompania;
	private String numCtaSibamex;
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getCodeCompania() {
		return codeCompania;
	}
	public void setCodeCompania(String codeCompania) {
		this.codeCompania = codeCompania;
	}
	public String getNumCtaSibamex() {
		return numCtaSibamex;
	}
	public void setNumCtaSibamex(String numCtaSibamex) {
		this.numCtaSibamex = numCtaSibamex;
	}
	@Override
	public String toString() {
		return "QiuboRecargasRequest [mobileNumber=" + mobileNumber + ", amount=" + amount + ", codeCompania="
				+ codeCompania + ", numCtaSibamex=" + numCtaSibamex + "]";
	}
}