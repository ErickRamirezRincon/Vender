package mx.com.fincomun.vendedores.bean.request;

import java.util.ArrayList;
import java.util.List;

public class VentaRequest extends Request {

	private float montoTotal;
	private String codigoQR;
	private float comision;
	private long idTipoPago;
	private long idVendedor;
	private String codigoServicio;
	private long idTipoServicio;
	private List<ProductoVenta> listaProductos = new ArrayList<ProductoVenta>();
	
	public float getMontoTotal() {
		return montoTotal;
	}
	public void setMontoTotal(float montoTotal) {
		this.montoTotal = montoTotal;
	}
	public String getCodigoQR() {
		return codigoQR;
	}
	public void setCodigoQR(String codigoQR) {
		this.codigoQR = codigoQR;
	}
	public float getComision() {
		return comision;
	}
	public void setComision(float comision) {
		this.comision = comision;
	}
	public long getIdTipoPago() {
		return idTipoPago;
	}
	public void setIdTipoPago(long idTipoPago) {
		this.idTipoPago = idTipoPago;
	}
	public long getIdVendedor() {
		return idVendedor;
	}
	public void setIdVendedor(long idVendedor) {
		this.idVendedor = idVendedor;
	}
	public List<ProductoVenta> getListaProductos() {
		return listaProductos;
	}
	public void setListaProductos(List<ProductoVenta> listaProductos) {
		this.listaProductos = listaProductos;
	}
	public long getIdTipoServicio() {
		return idTipoServicio;
	}
	public void setIdTipoServicio(long idTipoServicio) {
		this.idTipoServicio = idTipoServicio;
	}
	public String getCodigoServicio() {
		return codigoServicio;
	}
	public void setCodigoServicio(String codigoServicio) {
		this.codigoServicio = codigoServicio;
	}
	@Override
	public String toString() {
		return "VentaRequest [montoTotal=" + montoTotal + ", codigoQR=" + codigoQR + ", comision=" + comision
				+ ", idTipoPago=" + idTipoPago + ", idVendedor=" + idVendedor + ", codigoServicio=" + codigoServicio
				+ ", idTipoServicio=" + idTipoServicio + ", listaProductos=" + listaProductos + ", toString()="
				+ super.toString() + "]";
	}
}