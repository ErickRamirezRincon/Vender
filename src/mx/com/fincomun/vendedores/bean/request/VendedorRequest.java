package mx.com.fincomun.vendedores.bean.request;

public class VendedorRequest extends Request{
	
	private int id;
	private String nombre;
	private String numero;
	private String correo;
	private String usuario;
	private String contrasena;
	private long cuentaId;
	private long numCliente;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getContrasena() {
		return contrasena;
	}
	public void setContrasena(String contrasena) {
		this.contrasena = contrasena;
	}
	public long getCuentaId() {
		return cuentaId;
	}
	public void setCuentaId(long cuentaId) {
		this.cuentaId = cuentaId;
	}
	public long getNumCliente() {
		return numCliente;
	}
	public void setNumCliente(long numCliente) {
		this.numCliente = numCliente;
	}
	@Override
	public String toString() {
		return "VendedorRequest [id=" + id + ", nombre=" + nombre + ", numero=" + numero + ", correo=" + correo
				+ ", usuario=" + usuario + ", contrasena=" + contrasena + ", cuentaId=" + cuentaId + ", numCliente="
				+ numCliente + ", getIdUsuario()=" + getIdUsuario() + "]";
	}
}