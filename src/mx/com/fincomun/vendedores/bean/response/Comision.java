package mx.com.fincomun.vendedores.bean.response;

public class Comision {
	
	private boolean cobraComision;
	private String tipoComision;
	private String comisionFC;
	private String comisionBLM;
	
	public boolean isCobraComision() {
		return cobraComision;
	}
	public void setCobraComision(boolean cobraComision) {
		this.cobraComision = cobraComision;
	}
	public String getTipoComision() {
		return tipoComision;
	}
	public void setTipoComision(String tipoComision) {
		this.tipoComision = tipoComision;
	}
	public String getComisionFC() {
		return comisionFC;
	}
	public void setComisionFC(String comisionFC) {
		this.comisionFC = comisionFC;
	}
	public String getComisionBLM() {
		return comisionBLM;
	}
	public void setComisionBLM(String comisionBLM) {
		this.comisionBLM = comisionBLM;
	}
}